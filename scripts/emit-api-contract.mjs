#!/usr/bin/env node
// emit-api-contract.mjs · Wave C0.3 (260713, cutover program) · the VERSIONED, machine-readable
// producer contract for api.xlooop.com — the artifact x-ai-front (and the x-backend parity gates)
// pin by version + hash.
//
// Assembles docs/contracts/api-contract.v1.json from FOUR code-derived sections (never hand-written):
//   routes   — the deterministic /api/v1 surface via scripts/emit-routes-manifest.mjs (169 templates,
//              contract_version + contract_hash stamped by the emitter itself);
//   envelope — the DataClass vocabulary from src/workers/lib/response-envelope.ts (M3) + the
//              authority-envelope field names (M4);
//   errors   — the wire error envelope shape + the CODE_TO_STATUS table from
//              src/workers/middleware/error.ts (EE wave);
//   cors     — the allowed methods/headers/origin-pattern from src/workers/middleware/cors.ts.
//
// DETERMINISM CONTRACT: no timestamps, no commit SHAs, no environment values — regenerating at the
// same tree yields a byte-identical file, which is exactly what scripts/verify-api-contract-artifact.mjs
// (blocking, ci-local) asserts. Body schemas remain prose in docs/architecture/backend/API_CONTRACT_V1.md
// (a documented gap, closed incrementally — see BACKEND_REPOSITORY_CUTOVER_PREFLIGHT.md).
//
// Usage:
//   node scripts/emit-api-contract.mjs            # write docs/contracts/api-contract.v1.json
//   node scripts/emit-api-contract.mjs --stdout   # print to stdout instead (used by the drift gate)

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';

const REPO = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(REPO, 'docs/contracts/api-contract.v1.json');

// ── routes: run the route emitter in-process-adjacent (single source of truth) ─────────────────
const routesJson = JSON.parse(
  execFileSync(process.execPath, [join(REPO, 'scripts/emit-routes-manifest.mjs')], { encoding: 'utf-8' }),
);

// ── envelope: DataClass union from response-envelope.ts ────────────────────────────────────────
const envSrc = readFileSync(join(REPO, 'src/workers/lib/response-envelope.ts'), 'utf-8');
const dcMatch = envSrc.match(/export type DataClass = ([^;]+);/);
const dataClasses = dcMatch ? [...dcMatch[1].matchAll(/'([^']+)'/g)].map((m) => m[1]) : [];
if (!dataClasses.length) throw new Error('DataClass union not found in response-envelope.ts');

// ── errors: wire shape + CODE_TO_STATUS from middleware/error.ts ───────────────────────────────
const errSrc = readFileSync(join(REPO, 'src/workers/middleware/error.ts'), 'utf-8');
const tableMatch = errSrc.match(/const CODE_TO_STATUS[^{]*\{([\s\S]*?)\};/);
if (!tableMatch) throw new Error('CODE_TO_STATUS not found in middleware/error.ts');
const codeToStatus = {};
for (const m of tableMatch[1].matchAll(/^\s*([A-Z0-9_]+):\s*(\d+),?\s*$/gm)) codeToStatus[m[1]] = Number(m[2]);
if (!Object.keys(codeToStatus).length) throw new Error('CODE_TO_STATUS parsed empty');

// ── cors: methods/headers/origin from middleware/cors.ts ───────────────────────────────────────
const corsSrc = readFileSync(join(REPO, 'src/workers/middleware/cors.ts'), 'utf-8');
const methodsMatch = corsSrc.match(/ALLOWED_METHODS\s*=\s*'([^']+)'/);
const headersMatch = corsSrc.match(/ALLOWED_HEADERS\s*=\s*'([^']+)'/);
const originMatch = corsSrc.match(/ALLOWED_ORIGIN_PATTERN\s*\|\|\s*'([^']+)'/);

const contract = {
  _comment:
    'GENERATED by scripts/emit-api-contract.mjs — the versioned producer contract for api.xlooop.com. Deterministic (no timestamps/SHAs); drift gate = verify:api-contract. Consumers pin contract_version + contract_hash. Do not hand-edit.',
  schema_id: 'xlooop.api_contract.v1',
  contract_version: routesJson._provenance.contract_version,
  contract_hash: routesJson._provenance.contract_hash,
  sources: {
    routes: 'scripts/emit-routes-manifest.mjs (src/workers/index.ts + routes/*.ts)',
    envelope: 'src/workers/lib/response-envelope.ts (M3) + lib/allowed-actions.ts (M4)',
    errors: 'src/workers/middleware/error.ts (EE wave)',
    cors: 'src/workers/middleware/cors.ts',
    body_schemas: 'docs/architecture/backend/API_CONTRACT_V1.md (prose; machine-readable = documented gap)',
  },
  routes: {
    route_count: routesJson._provenance.route_count,
    backed_count: routesJson._provenance.backed_count,
    backed: routesJson.backed,
    routes: routesJson.routes,
  },
  envelope: {
    data_class: { field: 'data_class', values: dataClasses, ssot: 'docs/security/DATA_CLASSIFICATION.md' },
    authority: { fields: ['allowed_actions', 'disabled_reasons'], note: 'M4 withAuthority envelope — allowed_actions: string[]; disabled_reasons: Record<action, reason>' },
  },
  errors: {
    wire_shape: { error: 'string (human-safe message)', code: 'string (machine-readable, see code_to_status)', request_id: 'string (CF-Ray)' },
    code_to_status: codeToStatus,
  },
  cors: {
    allowed_origin_pattern: originMatch ? originMatch[1] : 'https://*.xlooop.com',
    allowed_methods: methodsMatch ? methodsMatch[1].split(',').map((s) => s.trim()) : null,
    allowed_headers: headersMatch ? headersMatch[1].split(',').map((s) => s.trim()) : null,
    preflight_max_age_hours: 24,
  },
  auth: {
    scheme: 'Authorization: Bearer <Clerk JWT> (verified via @clerk/backend verifyToken; JWKS cached 5 min)',
    unauthenticated: 401,
    missing_org: 403,
    idempotency: 'writes accept an Idempotency-Key header (uniform rule, FRONTIER-MANIFEST-260711)',
  },
  streaming: { present: false, note: 'no SSE/ReadableStream endpoints exist; live-stream routes are poll-based JSON snapshots' },
};

const json = JSON.stringify(contract, null, 2) + '\n';
if (process.argv.includes('--stdout')) {
  process.stdout.write(json);
} else {
  writeFileSync(OUT, json);
  console.log(`emit-api-contract · wrote ${OUT.replace(REPO + '/', '')} · ${contract.routes.route_count} routes · hash ${contract.contract_hash.slice(0, 12)}…`);
}
